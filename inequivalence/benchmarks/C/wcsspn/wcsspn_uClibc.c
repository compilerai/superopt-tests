#include <features.h>
#include <string.h>
#include <limits.h>
#include <stdint.h>

# include <wchar.h>
# include <wctype.h>

size_t wcsspn(const wchar_t *s1, const wchar_t *s2)
{
	register const wchar_t *s = s1;
	register const wchar_t *p = s2;

	while (*p) {
		if (*p++ == *s) {
			++s;
			p = s2;
		}
	}
	return s - s1;
}
